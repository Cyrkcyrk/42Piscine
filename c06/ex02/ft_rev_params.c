#include <unistd.h>

int		main(int ac, char **av)
{
	int i;

	while (--ac > 0)
	{
		i = -1;
		while (av[ac][++i] != '\0')
			write(1, &av[ac][i], 1);
		write(1, "\n", 1);
	}
	return (0);
}
