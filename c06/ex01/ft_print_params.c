#include <unistd.h>

int		main(int ac, char **av)
{
	int i;
	int j;

	ac--;
	av++;
	j = -1;
	while (++j < ac)
	{
		i = -1;
		while (av[j][++i] != '\0')
			write(1, &av[j][i], 1);
		write(1, "\n", 1);
	}
	return (0);
}
